package arrow

trait _ApplicativeArrow
        extends _Arrow {
  type Op[-X, +Y] <: ApplicativeArrow[X, Y]

  /**
   * basic module functionality
   *
   * the resulting computation transforms
   * applies a computation to a value
   */
  def _app[X, Y]: Op[(Op[X, Y], X), Y]

  trait ApplicativeArrow[-X, +Y]
          extends Arrow[X, Y] {
    self: Op[X, Y] =>
  }
}
