package pure

/*
 * computations returning a value
 */

trait _Pure {
  /*
   * Fun is a type function turning a
   * type
   * into a
   * computation returning a type
   *
   */
  type Fun[+X] <: Pure[X]

  /**
   * basic module functionality
   *
   * embedding a
   * value
   * into the world of
   * computations
   *
   * the resulting pure computation simply
   * returns the value
   */
  def _pure[Y]: Y => Fun[Y]

  trait Pure[+X] {
    self: Fun[X] =>
  }
}
